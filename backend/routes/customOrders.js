// routes/customOrders.js
const express = require('express');
const path = require('path');
const multer = require('multer');
const ctrl = require('../controllers/customOrdersController');
const payCtrl = require('../controllers/customPaymentsController');
const router = express.Router();

/** ===================== Orders ===================== */
// ‡∏õ‡∏£‡∏∞‡πÄ‡∏°‡∏¥‡∏ô‡∏£‡∏≤‡∏Ñ‡∏≤
router.post('/orders/estimate', ctrl.estimatePrice);

// ‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏≠‡∏≠‡πÄ‡∏î‡∏≠‡∏£‡πå‡∏™‡∏±‡πà‡∏á‡∏ó‡∏≥
router.post('/orders', ctrl.createOrder);

// ‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£‡∏≠‡∏≠‡πÄ‡∏î‡∏≠‡∏£‡πå (admin ‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î ‡∏´‡∏£‡∏∑‡∏≠‡∏ú‡πà‡∏≤‡∏ô ?user_id= ‡πÅ‡∏™‡∏î‡∏á‡∏Ç‡∏≠‡∏á‡∏ú‡∏π‡πâ‡πÉ‡∏ä‡πâ)
router.get('/orders', ctrl.listOrders);

// ‡∏£‡∏≤‡∏¢‡∏•‡∏∞‡πÄ‡∏≠‡∏µ‡∏¢‡∏î‡∏≠‡∏≠‡πÄ‡∏î‡∏≠‡∏£‡πå
router.get('/orders/:id', ctrl.getOrderById);

// ‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï‡∏™‡∏ñ‡∏≤‡∏ô‡∏∞ (‡∏û‡∏´‡∏π‡∏û‡∏à‡∏ô‡πå - ‡πÄ‡∏™‡πâ‡∏ô‡∏ó‡∏≤‡∏á‡∏´‡∏•‡∏±‡∏Å‡πÉ‡∏´‡∏°‡πà)
router.put('/orders/:id/status', ctrl.updateOrderStatus);

// üîÅ ‡πÄ‡∏™‡πâ‡∏ô‡∏ó‡∏≤‡∏á‡πÄ‡∏î‡∏¥‡∏° (‡πÄ‡∏≠‡∏Å‡∏û‡∏à‡∏ô‡πå) ‡πÄ‡∏Å‡πá‡∏ö‡πÑ‡∏ß‡πâ‡∏ä‡∏±‡πà‡∏ß‡∏Ñ‡∏£‡∏≤‡∏ß‡πÄ‡∏û‡∏∑‡πà‡∏≠ compatibility
router.put('/order/:id/status', ctrl.updateOrderStatus);


/** ===================== Payments (Custom Orders) ===================== */
// ‡∏ó‡∏µ‡πà‡πÄ‡∏Å‡πá‡∏ö‡πÑ‡∏ü‡∏•‡πå‡∏™‡∏•‡∏¥‡∏õ
const slipDir = path.join(__dirname, '..', 'public', 'uploads', 'custom_payments');
const fs = require('fs');

// ‡∏™‡∏£‡πâ‡∏≤‡∏á‡πÇ‡∏ü‡∏•‡πÄ‡∏î‡∏≠‡∏£‡πå‡∏ñ‡πâ‡∏≤‡∏¢‡∏±‡∏á‡πÑ‡∏°‡πà‡∏°‡∏µ
if (!fs.existsSync(slipDir)) {
  fs.mkdirSync(slipDir, { recursive: true });
}

// ‡∏ï‡∏±‡πâ‡∏á‡∏Ñ‡πà‡∏≤ multer ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡πÑ‡∏ü‡∏•‡πå‡∏™‡∏•‡∏¥‡∏õ
const upload = multer({
  dest: slipDir,
  limits: {
    fileSize: 5 * 1024 * 1024, // ‡∏à‡∏≥‡∏Å‡∏±‡∏î‡∏Ç‡∏ô‡∏≤‡∏î‡πÑ‡∏ü‡∏•‡πå 5MB
  },
  fileFilter: (req, file, cb) => {
    // ‡∏≠‡∏ô‡∏∏‡∏ç‡∏≤‡∏ï‡πÄ‡∏â‡∏û‡∏≤‡∏∞‡πÑ‡∏ü‡∏•‡πå‡∏£‡∏π‡∏õ‡∏†‡∏≤‡∏û
    if (file.mimetype.startsWith('image/')) {
      cb(null, true);
    } else {
      cb(new Error('‡∏≠‡∏ô‡∏∏‡∏ç‡∏≤‡∏ï‡πÄ‡∏â‡∏û‡∏≤‡∏∞‡πÑ‡∏ü‡∏•‡πå‡∏£‡∏π‡∏õ‡∏†‡∏≤‡∏û‡πÄ‡∏ó‡πà‡∏≤‡∏ô‡∏±‡πâ‡∏ô'), false);
    }
  }
});

// ‡∏•‡∏π‡∏Å‡∏Ñ‡πâ‡∏≤‡∏≠‡∏±‡∏õ‡πÇ‡∏´‡∏•‡∏î‡∏™‡∏•‡∏¥‡∏õ‡πÉ‡∏´‡πâ order ‡∏ó‡∏µ‡πà‡∏Å‡∏≥‡∏•‡∏±‡∏á "waiting_payment"
router.post('/orders/:id/payments', upload.single('image'), (err, req, res, next) => {
  // ‡∏à‡∏±‡∏î‡∏Å‡∏≤‡∏£ error ‡∏à‡∏≤‡∏Å multer
  if (err instanceof multer.MulterError) {
    if (err.code === 'LIMIT_FILE_SIZE') {
      return res.status(400).json({ message: '‡∏Ç‡∏ô‡∏≤‡∏î‡πÑ‡∏ü‡∏•‡πå‡πÉ‡∏´‡∏ç‡πà‡πÄ‡∏Å‡∏¥‡∏ô‡πÑ‡∏õ (‡∏™‡∏π‡∏á‡∏™‡∏∏‡∏î 5MB)' });
    }
    return res.status(400).json({ message: '‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡∏≠‡∏±‡∏õ‡πÇ‡∏´‡∏•‡∏î‡πÑ‡∏ü‡∏•‡πå' });
  } else if (err) {
    return res.status(400).json({ message: err.message });
  }
  next();
}, payCtrl.createPayment);

// ‡∏î‡∏π‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£‡∏™‡∏•‡∏¥‡∏õ‡∏Ç‡∏≠‡∏á‡∏≠‡∏≠‡πÄ‡∏î‡∏≠‡∏£‡πå
router.get('/orders/:id/payments', payCtrl.listPaymentsByOrder);

// ‡πÅ‡∏≠‡∏î‡∏°‡∏¥‡∏ô‡∏≠‡∏ô‡∏∏‡∏°‡∏±‡∏ï‡∏¥‡∏™‡∏•‡∏¥‡∏õ -> order ‡πÑ‡∏õ‡∏™‡∏ñ‡∏≤‡∏ô‡∏∞ "paid"
router.put('/payments/:paymentId/approve', payCtrl.approvePayment);

// ‡πÅ‡∏≠‡∏î‡∏°‡∏¥‡∏ô‡∏õ‡∏è‡∏¥‡πÄ‡∏™‡∏ò‡∏™‡∏•‡∏¥‡∏õ (‡πÅ‡∏ô‡∏ö note ‡πÑ‡∏î‡πâ) -> order ‡∏Ñ‡∏á "waiting_payment"
router.put('/payments/:paymentId/reject', payCtrl.rejectPayment);

// ‡∏•‡πâ‡∏≤‡∏á‡πÑ‡∏ü‡∏•‡πå‡πÄ‡∏Å‡πà‡∏≤‡∏ó‡∏µ‡πà‡πÑ‡∏°‡πà‡πÉ‡∏ä‡πâ‡πÅ‡∏•‡πâ‡∏ß (‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡πÅ‡∏≠‡∏î‡∏°‡∏¥‡∏ô)
router.post('/cleanup-files', payCtrl.cleanupOldFiles);


module.exports = router;
